							new feature, a large contribution may be
							unavoidable.)
						</li>
						<li>
							Be sure to comment heavily and include a
							CHANGELOG, as it speeds up the review
							process. Also, include your real name.
						</li>
						<li>
							Export your changes to a patch file. We are
							unlikely to accept pull requests on Github.
						</li>
						<li>
							Email your patch file to us. By submitting
							code, you are agreeing to the
							<a href="/termsofdevelopment"
							target=_blank>Terms of Development</a>.
						</li>
						<li>
							We will review your contribution. If we
							choose to use any part of it, your name
							will be added to the list of contributors.
