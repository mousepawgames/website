						How do I contribute code?
					</b></p>
					<div class="hiddenDiv"><p class="txtReg">
						<ol>
						<li>
							Obtain a copy of the project code from
							<a href="https://github.com/mousepawgames" target=_blank>
								Github.
							</a>
						</li>
						<li>
							Make your changes to the code. Keep a single
							patch as simple as possible. We review every
							change manually. We're more likely to
							accept smaller changes than larger ones.
