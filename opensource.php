				<div class="expander" tabindex=-1>
					<p class="txtReg"><b>
						Why aren't you closed-source like most game
						companies?
					</b></p>
					<div class="hiddenDiv"><p class="txtReg">
						For the past 25+ years, the educational software
						industry has been dominated by a profit-first,
						closed-source model. In many cases, this has
						produced expensive software with design flaws
						and bugs, destined to be discontinued rather
						than improved.
					</p>
					<p class="txtReg">
						We envision a game engine that allows our games,
						and anything else anyone builds using it,
						to survive the waves of changing technology.
						Maybe our games can even outlive the company
						itself!
					</p></div>
				</div>

				<div class="expander" tabindex=-1>
					<p class="txtReg"><b>
						If your code is all freely available, how
						can you even make money?
					</b></p>
					<div class="hiddenDiv"><p class="txtReg">
						Open source is an amazing idea! There are a few
						ways to stay open-source and run a viable
						software company. Our method is best described
						as "free the code, sell the content." We aren't
						selling the code that runs <i>Operation SpyRat</i>;
						we are selling the story, animations, and educational
						content within it. It's a little like selling a
						movie you made, but giving away the awesome
						film-making equipment you used to make it.
					</p></div>
				</div>

				<div class="expander" tabindex=-1>
					<p class="txtReg"><b>
						Why aren't you <i>completely</i> open source?
					</b></p>
					<div class="hiddenDiv"><p class="txtReg">
						Making high-quality educational software
						requires a lot of work, and we believe
						we can best accomplish it as a company.
						In order to pay our wonderful team and keep
						the lights on, we need to make money somehow,
						and we believe that selling many of the games
						themselves is the best way to do that.
					</p>
					<p class="txtReg">
						In short, our <i>code</i> is open-source,
						while our <i>content and graphics</i> are
						closed-source. We ultimately sell the final
						combination thereof.
					</p></div>
				</div>

				<div class="expander" tabindex=-1>
					<p class="txtReg"><b>
						Why &quot;Open Source&quot; instead of
						&quot;Free Software&quot;?
					</b></p>
					<div class="hiddenDiv"><p class="txtReg">
						We were inspired to join the movement often
						collectively called &quot;FOSS&quot; (Free and
						Open Source Software) by Eric S. Raymond's
						<a href="http://www.catb.org/esr/writings/cathedral-bazaar/" target=_blank>
							<i>Cathedral and the Bazaar</i>
						</a>. As far as we
						can tell, the open source vision has the best
						track record of the two for leveling walls and
						encouraging collaboration among people,
						FOSS groups, and software companies. That's
						why we have thrown our support behind the
						<a href="http://www.opensource.org" target=_blank>
							Open Source Initiative
						</a>.
					</p>
					<p class="txtReg">
						While we recognize that the &quot;Free
						Software&quot; movement has spawned many great
						pieces of software, it also has a long history
						of building walls of distrust against
						well-meaning software companies, and even between
						FOSS groups themselves. We want to make peace
						and software, not join another war.
					</p></div>
				</div>

				<div class="expander" tabindex=-1>
					<p class="txtReg"><b>
						Why do you use the GPL, then?
					</b></p>
					<div class="hiddenDiv"><p class="txtReg">
						The GPL, or "General Public License", is
						the core of the Free Software movement. While
						we don't agree with the doctrine behind the
						license, we recognize that it <i>can</i> be
						used judiciously to further the place of
						free and open source software in the market.
					</p>
					<p class="txtReg">
						We only use the GPL for certain major pieces
						of our game engine, to ensure that they are
						only used for free to create more free and open
						source [FOSS] projects. If anyone wants to use
						our game engine for a closed-source commercial
						product, they can purchase a license. Part
						of those proceeds will be donated to the
						Open Source Initiative. Either way, FOSS wins!
					</p></div>
				</div>

				<div class="expander" tabindex=-1>
					<p class="txtReg"><b>
						Why this licensing setup?
					</b></p>
					<div class="hiddenDiv"><p class="txtReg">
						We are strong proponents of the Open Source
						movement (contrasted with the Free Software
						movement.) However, we believe that our
						development staff deserves to get paid a
						living wage down the road (once we start
						selling stuff.) Thus, if someone else wants to
						profit from the fruits of their labors without
						also sharing in kind, our developers deserve
						a piece of that pie.
					</p>
					<p class="txtReg">
					It is worth noting that a part of those profits
					are also earmarked for supporting open-source
					software projects that we use.
				</p></div>
				</div>

