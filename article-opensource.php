							<p class="txtHead1">MousePaw Games Goes Open Source</p>
							<p class="txtSmall">06/24/2015</p>
							<p class="txtReg">After much consideration and planning, MousePaw Games is making its platform open-source. Their entire platform – Trailcrest, Anari, Ratscript, and Lightrift – will be available under the GNU General Public License 3.0. MousePaw Games is also putting their support behind the <a href="http://opensource.org/" target=_blank>Open Source Initiative.</a></p>
							<p class="txtReg">The move is only natural, as MousePaw Games is fueled almost exclusively by open-source projects such as Ubuntu, LibreOffice, C++, Python, GIMP, and Audacity.</p>
							<p class="txtReg">Eric S. Raymond's watershed book “The Cathedral and the Bazaar” was the final tipping point, said Jason C. McDonald. “Our vision for MousePaw Games is parallel in many ways to the open source movement. The proprietary mindset is ultimately what killed the last generation of educational software. We don't want to repeat that history by making the same mistake.”</p>
							<p class="txtReg">The decision to use the term “open source” over "free software" in regards to this move is an intentional one, as it reflects MousePaw Games' stance that while code should be freely available, proprietary software is not an enemy to be destroyed. Some things are worth protecting.</p>
							<p class="txtReg">“The games themselves – the characters, the story, the graphics, the content – those are all things that we don't believe should be freely available,” McDonald explains. “Keeping Operation SpyRat closed-source allows us to make a fair profit to pay our hard-working development team, and ensure that intellectual property is always true to our mission statement. Opening that up wouldn't benefit anyone.”</p>
							<p class="txtReg">As for the open-source software, MousePaw Games will be offering an option for developers who want to use the code, but cannot make their own project open-source as required by the GNU General Public License. Once the projects are ready for release, developers will be able to purchase proprietary licenses from MousePaw Games. Some of the profits from these licenses will go to support the open source movement.</p>
							<p class="txtReg">The company will be maintaining mirrors of all its open-source projects on Github. Developers are encouraged to get involved. More details can be found <a href="http://www.mousepawgames.com/getinvolved/opensource">here.</a></p>
