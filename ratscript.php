							<p class="txtReg">Ratscript is MousePaw Games' premiere runtime dynamic programming language, originally developed for the Trailcrest game engine. The main advantage of Ratscript is that <b>it can be adapted to your project</b>, instead of your project adapting to it.</p>
							<p class="txtHead2">Gentle for beginners.</p>
							<p class="txtReg">Ratscript was written for non-programmers. It has a visual, obvious syntax that takes the guesswork out of code. This makes Ratscript ideal for teaching the principles of computer programming.</p>
							<p class="txtHead2">Burly for experts.</p>
							<p class="txtReg">Ratscript was originally designed to handle complex game content generation tasks. To that aim, it is extensible, object-oriented, and includes a robust standard library. Unlike many runtime languages, Ratscript can be adapted to work with your project.</p>
							<p class="txtHead2">Nimble like a rat.</p>
							<p class="txtReg">Whether you work in Windows, Mac, Linux, or another Unix operating system, Ratscript is ready to go! Ratscript is written in C++ for maximum cross-compatibility. Since it runs as a standalone command-line program, it can be used by itself, or as a subprocess under any capable platform (including C++, Python, and Adobe AIR.)</p>
							<p class="txtHead2">Uses for Ratscript:</p>
							<ul class="list">
