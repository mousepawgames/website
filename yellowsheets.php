							<p class="txtHead2">What are Yellow Sheets?</p>
							<p class="txtReg">No, they're not yellow (unless you printed them on that color paper.) Back when I was taking Pre-Calculus, I had a five subject notebook I used for my class notes. I would write especially important facts on the yellow divider pages, so I could find them easily later. Since then, I have frequently referenced those “yellow sheets” while tutoring at our local community college, often copying them down for the tutee for keep. Finally, I decided to create a high-quality set of these “yellow sheets”, modeled after the charts I have successfully used in tutoring.</p>
							<p class="txtReg">Thus, “Yellow Sheets” refers to the theory of content: these one-page charts and graphs contain only that information which you would write on a notebook divider page in your class notes.</p>
							<p class="txtReg">Yellow Sheets are created using <a href="http://www.geogebra.org/" target=_blank>GeoGebra</a>, <a href="http://www.libreoffice.org/" target=_blank>LibreOffice</a>, and <a href="http://www.gimp.org/" target=_blank>GIMP</a>.</p>
