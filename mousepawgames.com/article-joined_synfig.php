							On November 19, Jason C. McDonald made the call to abandon
							Adobe Flash as a platform immediately. The timing of the
							move was a surprise to everyone, even him. The company had
							been planning to migrate to open-source for the second title
							in the Operation SpyRat series, “The Phantom Orchestra,” but
							there were mounting signals that the Flash platform would not
							be a good option for this first game, either.
						</p>
						<p class="txtReg">
							“There have been rumors of Adobe dropping support of their
							Flash platform for quite some time. While these claims are
							officially denied, the signs all point towards the end of
							the SWF format,” McDonald explains. “They've been pressuring
							everyone to use HTML5 instead, and have just made SWF an
							open-source format. We can't take the risk of support being
							dropped – we would have to rebuild practically the entire
							game from scratch.”
						</p>
						<p class="txtReg">
							After some careful consideration, MousePaw Games settled on
							an open-source animation platform to replace Adobe Flash –
							Synfig Studio. Being a mature application, Synfig seemed the
							best candidate for the cinematic animation Operation SpyRat
							would need. There was only one problem – Synfig had absolutely
							no support for interactivity.
						</p>
						<p class="txtReg">
							However, McDonald is confident. “How this has worked out is purely
							providential. We had already started migrating Ratscript to C++.
							That language has all of the features we need to build a full
							scripting engine into Synfig Studio. It'll take a lot of hard work,
							but I believe the payoff will be more than worth it.”
						</p>
						<p class="txtReg">
							MousePaw Games has forked Synfig Studio on GitHub, and will be focusing
