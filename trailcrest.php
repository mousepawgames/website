							<p class="txtHead2">Intelligent Level Adjustment</p>
							<p class="txtReg">As play progresses, Trailcrest automatically adjusts the content difficulty to ensure the player is receiving the best material for them. When it detects that content is too hard, it will lower the level; when the player excels over time, the level is raised. Level lockout ensures the content is never too easy - when a player has proven mastery of a level, it can only be accessed by a manual teacher override.</p>
							<p class="txtHead2">Customized Content</p>
							<img class="staffphotoLeft" src="http://www.mousepawgames.com/img/dictionary.jpg" width=300px/>
							<p class="txtReg">Trailcrest doesn't just manage broad concept levels. Our state-of-the-art content engine is designed to tailor material to meet each player's unique needs and capabilities. It tracks progress down to each individual word, and uses that information to intelligently select content from our massive database.</p>
							<p class="txtHead2">Universal Save Files</p>
							<p class="txtReg">Our entire Operation SpyRat series uses universal save files. A player can create an agent profile in one game, and use that same profile in all other games and grade levels in the series. Common settings are applied globally, and educators can access scores across multiple subjects and grade levels with one click.</p>
							<p class="txtHead2">Automated Help System</p>
							<img class="staffphotoRight" src="http://www.mousepawgames.com/img/mistake.jpg" width=300px/>
							<p class="txtReg">Trailcrest can detect when a player is having extra trouble with a concept, and will automatically provide help. We don't mean the vague hints seen in many games. Animated characters explain tough concepts in detail, while brief tutorial animations ensure full understanding of all of the game's activities.</p>
							<p class="txtHead2">Complete Teacher Control</p>
							<p class="txtReg">No computer is perfect, and none can read minds. With this in mind, Trailcrest gives educators the ultimate control over the game. Levels can be adjusted freely. Level adjustment parameters, auto help features, word lists...all of it is in the teacher's grasp. Changes can be applied to specific students, and teacher controls password protected to prevent tampering.</p>
